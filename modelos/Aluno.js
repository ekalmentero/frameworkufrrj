"use strict";
export class Grade{

    Constructor(id, nome, matricula, ativo, ingresso, deleted){
        this.id = id;
        this.nome = nome;
        this.matricula = matricula;
        this.ativo = ativo;
        this.ingresso = ingresso;
        this.deleted = deleted;
    }
     getId() {
        return this.id;
      }
      
      getNome() {
        return this.id;
      }
      getMatricula() {
        return this.id;
      }
      getAtivo() {
        return this.id;
      }
      getIngresso() {
        return this.id;
      }
      getDeleted() {
        return this.id;
      }
      
      setId(id) {
        this.id = id;
      }
      setNome(nome) {
        this.nome = nome;
      }
      setMatricula(matricula) {
        this.matricula = matricula;
      }
      setAtivo(ativo) {
        this.ativo = ativo;
      }
      setIngresso(ingresso) {
        this.ingresso = ingresso;
      }
      setDeleted(deleted) {
        this.deleted = deleted;
      }
      

}

